#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*copy;

	if (lst && f)
	{
		copy = lst;
		while (copy)
		{
			f(copy->content);
			copy = copy->next;
		}
	}
}
